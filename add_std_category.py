def classify(project_name: str):
    """返回 (小类, 大类) 或 (None, None)"""
    if not project_name:
        return None, None
    for minor, major, must_any, must_not in RULES:
        if any(kw in project_name for kw in must_any):
            if not any(kw in project_name for kw in must_not):
                return minor, major
    return None, None


DB_DIR = Path(__file__).parent / "data"
SITES = [
    "jszbcg", "yancheng_gov", "ycggzy", "sufu",
    "yueda", "dushi", "jscn", "chennan",
    "dongfang", "bigdata", "jingkai", "kaifaqu",
]

total_all = filled_all = 0

for site in SITES:
    db_path = DB_DIR / f"{site}.db"
    if not db_path.exists():
        continue
    conn = sqlite3.connect(db_path)
    conn.row_factory = sqlite3.Row
    cur = conn.cursor()

    cols = [r[1] for r in cur.execute("PRAGMA table_info(notices)").fetchall()]
    for col in ["proj_minor_cat", "proj_major_cat"]:
        if col not in cols:
            cur.execute(f"ALTER TABLE notices ADD COLUMN {col} TEXT")

    cur.execute("UPDATE notices SET proj_minor_cat=NULL, proj_major_cat=NULL")
    rows = cur.execute("SELECT id, project_name FROM notices").fetchall()

    updates = []
    for row in rows:
        minor, major = classify(row["project_name"])
        updates.append((minor, major, row["id"]))

    cur.executemany(
        "UPDATE notices SET proj_minor_cat=?, proj_major_cat=? WHERE id=?", updates
    )
    conn.commit()

    total = len(rows)
    filled = sum(1 for m, _, _ in updates if m)
    total_all += total
    filled_all += filled
    print(f"[{site:<12}] {filled}/{total}")
    conn.close()

print(f"\n已分类: {filled_all}/{total_all} ({filled_all*100//total_all}%)")

