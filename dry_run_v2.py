def classify(project_name):
    if not project_name:
        return None, None
    for minor, major, must_any, must_not in RULES:
        if any(kw in project_name for kw in must_any):
            if not any(kw in project_name for kw in must_not):
                return minor, major
    return None, None


DB_DIR = Path("/Users/yc/.openclaw/workspace/yancheng-bidding-pro/data")
SITES = [
    "jszbcg", "yancheng_gov", "ycggzy", "sufu",
    "yueda", "dushi", "jscn", "chennan",
    "dongfang", "bigdata", "jingkai", "kaifaqu",
]

results = defaultdict(list)
for site in SITES:
    db_path = DB_DIR / f"{site}.db"
    if not db_path.exists():
        continue
    conn = sqlite3.connect(db_path)
    for row in conn.execute("SELECT project_name FROM notices"):
        pname = row[0]
        minor, major = classify(pname)
        if minor:
            results[(major, minor)].append(pname)
    conn.close()

major_order = ["物业外包服务", "市政工程", "项目相关服务"]
by_major = defaultdict(dict)
for (major, minor), names in results.items():
    by_major[major][minor] = sorted(names)

for major in major_order:
    if major not in by_major:
        continue
    total = sum(len(v) for v in by_major[major].values())
    print(f"\n{'═'*62}")
    print(f"【{major}】共 {total} 条")
    print(f"{'═'*62}")
    for minor, names in by_major[major].items():
        print(f"\n── {minor}（{len(names)}条）")
        for n in names:
            print(f"  {n}")
